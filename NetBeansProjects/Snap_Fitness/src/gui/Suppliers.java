    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:

        if (evt.getClickCount() == 1) {

            int selectedrow = jTable1.getSelectedRow();

            sid = jTable1.getValueAt(selectedrow, 0).toString();

            jTextField1.setText(jTable1.getValueAt(selectedrow, 1).toString());
            jTextField2.setText(jTable1.getValueAt(selectedrow, 2).toString());
            jTextField3.setText(jTable1.getValueAt(selectedrow, 3).toString());

            jComboBox1.setSelectedItem(jTable1.getValueAt(selectedrow, 4).toString());

            jButton2.setEnabled(true);
            jButton1.setEnabled(false);
        } else if (evt.getClickCount() == 2) {

            int selectedrow = jTable1.getSelectedRow();

            if (grn != null) {

                grn.jLabel4.setText(sid);
                grn.jLabel6.setText(jTable1.getValueAt(selectedrow, 1).toString());
                grn.jLabel7.setText(jTable1.getValueAt(selectedrow, 2).toString());
                grn.jButton2.setEnabled(true);

                this.dispose();
            } else {
                mg.jLabel4.setText(sid);
                mg.jLabel6.setText(jTable1.getValueAt(selectedrow, 1).toString());
                mg.jLabel10.setText(jTable1.getValueAt(selectedrow, 4).toString());
                
                mg.jButton1.setEnabled(false);
                
                this.dispose();
            }

        }

    }//GEN-LAST:event_jTable1MouseClicked

