    public void loadTable() {
        try {
            ResultSet rs = MySQL.search("SELECT `suplier`.`id`, `suplier`.`name`, `suplier`.`mobile`, `suplier`.`email`, `company`.`name` AS `company_name` FROM `suplier` INNER JOIN `company` ON `suplier`.`company_id` = `company`.`id`");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("name"));
                v.add(rs.getString("mobile"));
                v.add(rs.getString("email"));
                v.add(rs.getString("company_name"));
                dtm.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

