    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String name = jTextField1.getText();
        String mobile = jTextField2.getText();
        String email = jTextField3.getText();
        String company = jComboBox1.getSelectedItem().toString();

        if (name.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please Enter the Supplier Name", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField1.grabFocus();
        } else if (mobile.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please Enter the Supplier Mobile Number", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField2.grabFocus();
        } else if (email.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please Enter the Supplier Email Address", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField3.grabFocus();
        } else if (!Pattern.compile("\\b[a-z0-9._%-]+@[a-z0-9.-]+\\.[a-z]{2,4}\\b").matcher(email).matches()) {
            JOptionPane.showMessageDialog(this, "Invalied Email Address", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField3.grabFocus();
        } else if (company.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select A Company", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField3.grabFocus();
        } else {
            try {

                ResultSet rs = MySQL.search("SELECT * FROM `suplier` WHERE `mobile` = '" + mobile + "'");
                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "This Supplier Already Exist", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    ResultSet companyrs = MySQL.search("SELECT `id` FROM `company` WHERE `name` = '" + company + "' ");
                    if (companyrs.next()) {
                        MySQL.iud("INSERT INTO `suplier` (`name`,`mobile`,`email`,`company_id`) VALUES('" + name + "','" + mobile + "','" + email + "','" + companyrs.getString("id") + "')");
                        clearFeilds();
                        JOptionPane.showMessageDialog(this, "New Supplier Added", "Success", JOptionPane.INFORMATION_MESSAGE);
                    }
                }

            } catch (Exception e) {
                e.printStackTrace();
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

