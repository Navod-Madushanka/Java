    public void loadschadule() {
        try {
            ResultSet rs = MySQL.search("SELECT DISTINCT(`schedule`.`name`) FROM `schedule`");
            DefaultTableModel dtm = (DefaultTableModel) jTable3.getModel();
            dtm.setRowCount(0);
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("name"));
                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void loadTable() {
        try {
            ResultSet rs = MySQL.search("SELECT `sets`.`id`,`body_part`.`name` AS `bname`, `exercises`.`name`, `sets`.`set`, `sets`.`reps` FROM `sets` INNER JOIN `exercises` ON `sets`.`exercises_id` = `exercises`.`id` INNER JOIN `body_part` ON `exercises`.`body_part_id` = `body_part`.`id` ORDER BY `sets`.`id` ASC");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("bname"));
                v.add(rs.getString("name"));
                v.add(rs.getString("set"));
                v.add(rs.getString("reps"));
                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

