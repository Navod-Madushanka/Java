    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:

        String name = jTextField4.getText();

        try {
            ResultSet srs = MySQL.search("SELECT * FROM `schedule` WHERE `name` = '" + name + "'");
            if (srs.next()) {
                JOptionPane.showMessageDialog(this, "This Schedule Name is Already Exits", "Warning", JOptionPane.WARNING_MESSAGE);
                jTextField4.setText("");
                jTextField4.grabFocus();
            } else {

                if (name.isBlank()) {
                    JOptionPane.showMessageDialog(this, "Plese Enter Schadule Name", "Warning", JOptionPane.WARNING_MESSAGE);
                    jTextField4.grabFocus();
                } else if (jTable2.getRowCount() == 0) {
                    JOptionPane.showMessageDialog(this, "Plese Select the Execise set in Table 1", "Warning", JOptionPane.WARNING_MESSAGE);
                    jTable1.grabFocus();
                } else {
                    for (int i = 0; i < jTable2.getRowCount(); i++) {
                        MySQL.iud("INSERT INTO `schedule` (`name`,`sets_id`) VALUES('" + name + "','" + jTable2.getValueAt(i, 0).toString() + "')");
                    }
                    loadschadule();
                    JOptionPane.showMessageDialog(this, "New Schadule Added", "Success", JOptionPane.INFORMATION_MESSAGE);
                }

            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton8ActionPerformed

