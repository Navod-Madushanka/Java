    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:

        if (evt.getClickCount() == 2) {

            int selectedrow = jTable1.getSelectedRow();

            boolean isfound = false;
            int x = -1;

            for (int i = 0; i < jTable2.getRowCount(); i++) {
                if (jTable2.getValueAt(i, 0).toString().equals(jTable1.getValueAt(selectedrow, 0))) {
                    isfound = true;
                    x = i;
                    break;
                }
            }

            if (isfound) {
                JOptionPane.showMessageDialog(this, "This Set is Already In the Schadule", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {

                DefaultTableModel dtm = (DefaultTableModel) jTable2.getModel();
                Vector v = new Vector();
                v.add(jTable1.getValueAt(selectedrow, 0));
                v.add(jTable1.getValueAt(selectedrow, 1));
                v.add(jTable1.getValueAt(selectedrow, 2));
                v.add(jTable1.getValueAt(selectedrow, 3));
                v.add(jTable1.getValueAt(selectedrow, 4));
                dtm.addRow(v);

            }

        }

    }//GEN-LAST:event_jTable1MouseClicked

