    public void loadStaffName() {
        try {
            ResultSet rs = MySQL.search("SELECT `name` FROM `staff`");
            Vector v = new Vector();
            v.add("Select");
            while (rs.next()) {
                v.add(rs.getString("name"));
            }
            DefaultComboBoxModel dcm = new DefaultComboBoxModel(v);
            jComboBox2.setModel(dcm);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void loadmachineName() {
        try {
            ResultSet rs = MySQL.search("SELECT `machine_brand`.`name` AS `brand_name`, `machine_category`.`name` AS `category_name`, `machine`.`name` FROM `machine` INNER JOIN `machine_category` ON `machine`.`machine_category_id` = `machine_category`.`id` INNER JOIN `machine_brand` ON `machine`.`machine_brand_id` = `machine_brand`.`id`");
            Vector v = new Vector();
            v.add("Select");
            while (rs.next()) {
                v.add(rs.getString("brand_name") + "-" + rs.getString("category_name") + "-" + rs.getString("name"));
            }
            DefaultComboBoxModel dcm = new DefaultComboBoxModel(v);
            jComboBox1.setModel(dcm);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

