    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        String machine_name = jComboBox1.getSelectedItem().toString();
        String destription = jTextArea1.getText();
        String staff_name = jComboBox2.getSelectedItem().toString();
        String price = jTextField1.getText();

        if (machine_name.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select the Machine Name", "Warning", JOptionPane.WARNING_MESSAGE);
            jComboBox1.grabFocus();
        } else if (destription.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please Select Add a Destription of the Maintanence", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextArea1.grabFocus();
        } else if (staff_name.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select the Staff Member", "Warning", JOptionPane.WARNING_MESSAGE);
            jComboBox2.grabFocus();
        } else if (price.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please Enter the Payment", "Warning", JOptionPane.WARNING_MESSAGE);
            jComboBox1.grabFocus();
        } else if (!Pattern.compile("[0][.][0-9]+|[1-9][0-9]*|[1-9][0-9]*[.][0-9]+").matcher(price).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid Payment", "Warning", JOptionPane.WARNING_MESSAGE);
            jComboBox1.grabFocus();
        } else {

            try {

                String[] name = machine_name.split("-");

                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                String date = sdf.format(new Date());
                
                String machine_id = "0";
                ResultSet rs = MySQL.search("SELECT `machine`.`id` FROM `machine` INNER JOIN `machine_category` ON `machine`.`machine_category_id` = `machine_category`.`id` INNER JOIN `machine_brand` ON `machine`.`machine_brand_id` = `machine_brand`.`id` WHERE `machine_category`.`name` = '"+name[1]+"' AND `machine_brand`.`name` = '"+name[0]+"' AND `machine`.`name` = '"+name[2]+"'");
                if(rs.next()){
                    machine_id = rs.getString("id");
                }
                
                String staff_id = "0";
                ResultSet staffrs = MySQL.search("SELECT `id` FROM `staff` WHERE `staff`.`name` = '"+staff_name+"'");
                if (staffrs.next()) {
                    staff_id = staffrs.getString("id");
                }

                MySQL.iud("INSERT INTO `maintenance` (`machine_id`,`destription`,`payment`,`staff_id`,`date`) VALUES('"+machine_id+"','"+destription+"','"+price+"','"+staff_id+"','"+date+"')");
                
                resetFields();
                
                JOptionPane.showMessageDialog(this, "Maintenance Recode added", "Success", JOptionPane.INFORMATION_MESSAGE);

            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_jButton2ActionPerformed

