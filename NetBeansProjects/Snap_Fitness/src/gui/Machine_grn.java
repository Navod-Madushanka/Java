    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:

        String payment_type = jComboBox3.getSelectedItem().toString();
        String payment = jTextField3.getText();

        if (jTable1.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Please Add Products", "Warning", JOptionPane.WARNING_MESSAGE);
            jButton2.grabFocus();
        } else if (payment_type.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select A Payment Type", "Warning", JOptionPane.WARNING_MESSAGE);
            jComboBox3.grabFocus();
        } else if (!Pattern.compile("(0|0[.]|0[.][0-9]*)|[1-9]|[1-9][0-9]*|[1-9][0-9]*[.]|[1-9][0-9]*[.][0-9]*").matcher(payment).matches()) {
            JOptionPane.showMessageDialog(this, "Invalied Payment", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField3.grabFocus();
        } else {

            //                grn insert
            String grn_id = String.valueOf(System.currentTimeMillis());

            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
            String date_of_grn = sdf.format(new Date());

            MySQL.iud("INSERT INTO `grn` (`id`,`suplier_id`,`date`) VALUES('" + grn_id + "','" + jLabel4.getText() + "','" + date_of_grn + "')");
//                grn insert

//                machine insert
            try {

                for (int i = 0; i < jTable1.getRowCount(); i++) {

                    String category = jTable1.getValueAt(i, 1).toString();
                    String brand = jTable1.getValueAt(i, 2).toString();
                    String name = jTable1.getValueAt(i, 3).toString();
                    String price = jTable1.getValueAt(i, 4).toString();

                    ResultSet categoryrs = MySQL.search("SELECT `id` FROM `machine_category` WHERE `name` = '" + category + "'");
                    String category_id = "";
                    if (categoryrs.next()) {
                        category_id = categoryrs.getString("id");
                    }

                    ResultSet brandrs = MySQL.search("SELECT `id` FROM `machine_brand` WHERE `name` = '" + brand + "'");
                    String brand_id = "";
                    if (brandrs.next()) {
                        brand_id = brandrs.getString("id");
                    }

                    MySQL.iud("INSERT INTO `machine` (`machine_category_id`,`machine_brand_id`,`name`,`grn_id`,`price`) VALUES ('" + category_id + "','" + brand_id + "','" + name + "','" + grn_id + "','" + price + "')");

                }
                reload();
                JOptionPane.showMessageDialog(this, "New Machine Added", "Success", JOptionPane.INFORMATION_MESSAGE);

            } catch (Exception e) {
                e.printStackTrace();
            }

//                machine insert
        }

    }//GEN-LAST:event_jButton5ActionPerformed

