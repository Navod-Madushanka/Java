    private void jTextField5KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField5KeyReleased
        // TODO add your handling code here:

        if (jTextField5.getText().isBlank()) {
            jLabel26.setText("0.00");
            jLabel26.setForeground(Color.BLACK);
        } else {
            String total = jLabel27.getText();
            String payment = jTextField5.getText();

            double balance = Double.parseDouble(payment) - Double.parseDouble(total);

            if (balance < 0) {
                jLabel26.setForeground(Color.red);
            } else {
                jLabel26.setForeground(Color.BLUE);
            }

            jLabel26.setText(df.format(balance));
        }

    }//GEN-LAST:event_jTextField5KeyReleased

