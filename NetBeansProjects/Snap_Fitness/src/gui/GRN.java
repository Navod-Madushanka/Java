    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        String supplier_id = jLabel4.getText();
        String supplier_Name = jLabel6.getText();
        String product_id = jLabel10.getText();
        String category = jLabel16.getText();
        String brand = jLabel14.getText();
        String name = jLabel12.getText();

        String qty = jTextField1.getText();
        String selling_price = jTextField3.getText();
        String buying_price = jTextField2.getText();
        Date mfdate = jDateChooser1.getDate();
        Date exdate = jDateChooser2.getDate();

        if (supplier_id.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please Select A Supplier", "Warning", JOptionPane.WARNING_MESSAGE);
            jButton1.grabFocus();
        } else if (product_id.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please Select A Product", "Warning", JOptionPane.WARNING_MESSAGE);
            jButton2.grabFocus();
        } else if (qty.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please Enter a Quantity", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField1.grabFocus();
        } else if (!Pattern.compile("[1-9][0-9]*").matcher(qty).matches()) {
            JOptionPane.showMessageDialog(this, "Invalied Quantity", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField1.grabFocus();
        } else if (selling_price.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please Enter a Selling Price", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField2.grabFocus();
        } else if (!Pattern.compile("[0][.][0-9]+|[1-9][0-9]*|[1-9][0-9]*[.][0-9]+").matcher(selling_price).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid Selling Price", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField2.grabFocus();
        } else if (buying_price.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please Enter a Buying Price", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField3.grabFocus();
        } else if (!Pattern.compile("[0][.][0-9]+|[1-9][0-9]*|[1-9][0-9]*[.][0-9]+").matcher(buying_price).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid Buying Price", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField2.grabFocus();
        } else if (Double.parseDouble(buying_price) >= Double.parseDouble(selling_price)) {
            JOptionPane.showMessageDialog(this, "Invalied Buying and Selling Price", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField2.grabFocus();
        } else if (mfdate == null) {
            JOptionPane.showMessageDialog(this, "Please Enter the Manufacture Date", "Warning", JOptionPane.WARNING_MESSAGE);
            jDateChooser1.grabFocus();
        } else if (exdate == null) {
            JOptionPane.showMessageDialog(this, "Please Enter the Expire Date", "Warning", JOptionPane.WARNING_MESSAGE);
            jDateChooser2.grabFocus();
        } else if (mfdate.after(new Date())) {
            JOptionPane.showMessageDialog(this, "Invalied Manufacture Date", "Warning", JOptionPane.WARNING_MESSAGE);
            jDateChooser1.grabFocus();
        } else if (exdate.before(new Date())) {
            JOptionPane.showMessageDialog(this, "Invalied Expire Date", "Warning", JOptionPane.WARNING_MESSAGE);
            jDateChooser2.grabFocus();
        } else {

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();

            String mfd = sdf.format(mfdate);
            String exd = sdf.format(exdate);

            boolean isFound = false;
            int x = -1;

            for (int i = 0; i < dtm.getRowCount(); i++) {
                String id = jTable1.getValueAt(i, 2).toString();

                if (id.equals(product_id)) {
                    isFound = true;
                    x = i;
                    break;
                }
            }

            if (isFound) {
                int option = JOptionPane.showConfirmDialog(this, "This Product is Already Added. Do you want to Update?", "Confirmation", JOptionPane.YES_OPTION, JOptionPane.INFORMATION_MESSAGE);

                if (option == JOptionPane.YES_OPTION) {
                    int oldqty = Integer.parseInt(jTable1.getValueAt(x, 4).toString());
                    int finalqty = oldqty + Integer.parseInt(qty);

                    jTable1.setValueAt(String.valueOf(finalqty), x, 4);
                    jTable1.setValueAt(buying_price, x, 5);

                    double updateditemtotal = finalqty * Double.parseDouble(buying_price);
                    jTable1.setValueAt(String.valueOf(updateditemtotal), x, 9);

                    updateTotal();
                }

                resetFields();

            } else {

                Vector v = new Vector();

                v.add(supplier_id);
                v.add(supplier_Name);
                v.add(product_id);
                v.add(name);
                v.add(qty);
                v.add(buying_price);
                v.add(selling_price);
                v.add(mfd);
                v.add(exd);

                double itemtotal = Integer.parseInt(qty) * Double.parseDouble(buying_price);

                v.add(df.format(itemtotal));

                dtm.addRow(v);

                updateTotal();

                resetFields();

                JOptionPane.showMessageDialog(this, "Product Added to GRN", "Success", JOptionPane.INFORMATION_MESSAGE);

            }

        }

    }//GEN-LAST:event_jButton3ActionPerformed

