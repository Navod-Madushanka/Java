    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        
        if (evt.getClickCount() == 2) {
            int selectedrow = jTable1.getSelectedRow();
            int option = JOptionPane.showConfirmDialog(this, "Do you want to remove this Member?", "Comfermation", JOptionPane.YES_NO_OPTION,JOptionPane.INFORMATION_MESSAGE);
            
            if (option == JOptionPane.YES_OPTION) {
                
                int status = 2;
                
                if(jTable1.getValueAt(selectedrow, 7).equals("Inactive")){
                    status = 1;
                }
                
                MySQL.iud("UPDATE `players` SET `status_id` = "+status+" WHERE `id` = '"+jTable1.getValueAt(selectedrow, 0)+"'");
                
                loadTable();
                
            }
        }
        
    }//GEN-LAST:event_jTable1MouseClicked

