    public void loadTable() {
        try {
            ResultSet rs = MySQL.search("SELECT `players`.`id`, `players`.`name`, `players`.`mobile`, `players`.`email`, `players_prograss`.`hight`, `players_prograss`.`weight`, `players_prograss`.`medical`, `status`.`name` AS `status_name` FROM `players` INNER JOIN `players_prograss` ON `players`.`id` = `players_prograss`.`players_id` INNER JOIN `status` ON `players`.`status_id` = `status`.`id` ORDER BY `players`.`id` ASC");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("Name"));
                v.add(rs.getString("mobile"));
                v.add(rs.getString("email"));
                v.add(rs.getString("hight"));
                v.add(rs.getString("weight"));
                v.add(rs.getString("medical"));
                v.add(rs.getString("status_name"));

                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

