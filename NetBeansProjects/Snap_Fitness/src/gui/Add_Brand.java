    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String name = jTextField1.getText();
        int selectedrow = jTable1.getSelectedRow();

        if (name.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please Enter A Brand Name", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField1.grabFocus();
        } else {
            try {
                ResultSet rs = MySQL.search("SELECT * FROM `brand` WHERE `name` = '" + name + "'");
                if (rs.next()) {
                    jTextField1.setText("");
                    JOptionPane.showMessageDialog(this, "This Brand is Already Excits", "Warning", JOptionPane.WARNING_MESSAGE);
                    jTextField1.grabFocus();
                } else {

                    MySQL.iud("UPDATE `brand` SET `name` = '" + name + "' WHERE `id` = '" + jTable1.getValueAt(selectedrow, 0).toString() + "'");
                    jTextField1.setText("");
                    loadTable();
                    JOptionPane.showMessageDialog(this, "Brand is  Updated", "Success", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        
        pr.loadbrand();

    }//GEN-LAST:event_jButton1ActionPerformed

