    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        String name = jTextField1.getText();

        if (name.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please Enter A Brand Name", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField1.grabFocus();
        } else {
            try {
                ResultSet rs = MySQL.search("SELECT * FROM `brand` WHERE `name` = '" + name + "'");
                if (rs.next()) {
                    jTextField1.setText("");
                    JOptionPane.showMessageDialog(this, "This Brand is Already Excits", "Warning", JOptionPane.WARNING_MESSAGE);
                    jTextField1.grabFocus();
                } else {

                    MySQL.iud("INSERT INTO `brand` (`name`) VALUES('" + name + "')");
                    jTextField1.setText("");
                    loadTable();
                    JOptionPane.showMessageDialog(this, "New Brand Added", "Success", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        
        pr.loadbrand();

    }//GEN-LAST:event_jButton2ActionPerformed

