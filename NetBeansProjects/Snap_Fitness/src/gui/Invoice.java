    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String mid = jTextField1.getText();

        if (mid.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please Enter the Member Id", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField1.grabFocus();
        } else {

            try {
                ResultSet rs = MySQL.search("SELECT * FROM `players` WHERE `id` = '" + mid + "' AND `status_id` != '2' ");

                if (rs.next()) {

                    jLabel5.setText(rs.getString("name"));
                    jLabel7.setText(rs.getString("mobile"));
                    jLabel8.setText(rs.getString("email"));

                    jButton1.setEnabled(false);
                    jTextField1.setEnabled(false);

                } else {
                    JOptionPane.showMessageDialog(this, "This Member is Not Excist OR Member is Inactive", "Warning", JOptionPane.WARNING_MESSAGE);
                    jTextField1.grabFocus();
                    jTextField1.grabFocus();
                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_jButton1ActionPerformed

