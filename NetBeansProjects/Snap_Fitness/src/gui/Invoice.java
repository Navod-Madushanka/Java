    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        mid = jLabel5.getText();
        String qty = jTextField2.getText();
        String sid = jLabel12.getText();

        if (mid.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please Select a Member First", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField1.grabFocus();
        } else if (sid.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please Select a Product on Stock Table", "Warning", JOptionPane.WARNING_MESSAGE);
            jButton2.grabFocus();
        } else if (qty.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please Enter the Quantity", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField2.grabFocus();
        } else if (!Pattern.compile("[1-9][0-9]*").matcher(qty).matches()) {
            JOptionPane.showMessageDialog(this, "Invalied Quantity", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField2.grabFocus();
        } else {

            double selling_price = Double.parseDouble(jLabel26.getText());

            try {

                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();

                boolean isfound = false;
                int x = -1;

                for (int i = 0; i < dtm.getRowCount(); i++) {
                    String id = jTable1.getValueAt(i, 1).toString();

                    if (id.equals(jLabel14.getText())) {
                        isfound = true;
                        x = i;
                        break;
                    }

                }

                if (isfound) {
                    int option = JOptionPane.showConfirmDialog(this, "This Product is Already Added. Do you want to Update?", "Confirmation", JOptionPane.YES_OPTION, JOptionPane.INFORMATION_MESSAGE);

                    if (option == JOptionPane.YES_OPTION) {
                        int oldqty = Integer.parseInt(jTable1.getValueAt(x, 5).toString());
                        int finalqty = oldqty + Integer.parseInt(qty);
                        jTable1.setValueAt(String.valueOf(finalqty), x, 5);
                        double updateditemtotal = finalqty * Double.parseDouble(jTable1.getValueAt(x, 9).toString());
                        jTable1.setValueAt(String.valueOf(updateditemtotal), x, 9);
                        updateTotal();
                    }
                    resetfields();

                } else {

                    Vector v = new Vector();
                    v.add(jLabel12.getText());
                    v.add(jLabel14.getText());
                    v.add(jLabel20.getText());
                    v.add(jLabel18.getText());
                    v.add(jLabel16.getText());
                    v.add(qty);
                    v.add(selling_price);
                    v.add(jLabel22.getText());
                    v.add(jLabel28.getText());

                    double total = Double.parseDouble(qty) * selling_price;

                    v.add(total);

                    dtm.addRow(v);

                    updateTotal();

                    resetfields();

                }

            } catch (Exception e) {
                e.printStackTrace();
            }
        }

    }//GEN-LAST:event_jButton3ActionPerformed

