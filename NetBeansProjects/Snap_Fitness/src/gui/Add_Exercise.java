    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        String name = jTextField1.getText();
        String bodypart = jComboBox1.getSelectedItem().toString();
        String sets = jTextField2.getText();
        String reps = jTextField3.getText();

        if (name.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please Enter Name of the Execise ", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField1.grabFocus();
        } else if (bodypart.equals("Select")) {
            JOptionPane.showMessageDialog(this, "PleaseSelect the Body Part ", "Warning", JOptionPane.WARNING_MESSAGE);
            jComboBox1.grabFocus();
        } else if (sets.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please Enter Sets ", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField2.grabFocus();
        } else if (!Pattern.compile("[0-9]*").matcher(sets).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid Set Size", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField2.grabFocus();
        } else if (reps.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please Enter Reps", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField2.grabFocus();
        } else if (!Pattern.compile("[0-9]*").matcher(reps).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid Reps Size", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField2.grabFocus();
        } else {

            try {

                String id = String.valueOf(System.currentTimeMillis());

                ResultSet brs = MySQL.search("SELECT `id` FROM `body_part` WHERE `name` = '" + bodypart + "'");
                String bid = "";
                if (brs.next()) {
                    bid = brs.getString("id");
                }

                MySQL.iud("INSERT INTO `exercises`(`id`,`body_part_id`,`name`) VALUES('" + id + "','" + bid + "','" + name + "')");

                MySQL.iud("INSERT INTO `sets`(`exercises_id`,`set`,`reps`) VALUES('" + id + "','" + sets + "','" + reps + "')");

                clearField();
                JOptionPane.showMessageDialog(this, "New Execise Saved", "Warning", JOptionPane.INFORMATION_MESSAGE);
                ex.loadTable();
                
            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_jButton2ActionPerformed

