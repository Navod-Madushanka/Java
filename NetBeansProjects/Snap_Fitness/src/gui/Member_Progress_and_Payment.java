    public void loadTable() {

        try {
            ResultSet rs = MySQL.search("SELECT `players`.`id`, `players`.`name`, `players_prograss`.`hight`, `players_prograss`.`weight`, `players_prograss`.`date`, `player_payment_type`.`price` FROM `players_prograss` INNER JOIN `players` ON `players_prograss`.`players_id` = `players`.`id` INNER JOIN `monthly_payment` ON `players_prograss`.`monthly_payment_id` = `monthly_payment`.`id` INNER JOIN `player_payment_type` ON `monthly_payment`.`player_payment_type_id` = `player_payment_type`.`id` WHERE `players`.`status_id` != '2' ORDER BY `players_prograss`.`date` ASC");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("name"));
                v.add(rs.getString("hight"));
                v.add(rs.getString("weight"));

                double h = Double.parseDouble(rs.getString("hight")) / 100;

                double d = h * h;
                double bmi = Double.parseDouble(rs.getString("weight")) / d;

                v.add(df.format(bmi));
                v.add(rs.getString("date"));
                v.add(rs.getString("price"));
                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

