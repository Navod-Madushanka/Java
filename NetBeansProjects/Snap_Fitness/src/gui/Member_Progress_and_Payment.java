    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        String member_id = jTextField1.getText();
        String hight = jTextField2.getText();
        String weight = jTextField3.getText();
        String medical = jTextArea1.getText();
        String schedule = jComboBox1.getSelectedItem().toString();
        String payment = jComboBox2.getSelectedItem().toString();

        if (member_id.isBlank()) {
            JOptionPane.showMessageDialog(this, "Plese Enter the Member's ID Number", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField1.grabFocus();
        } else if (hight.isBlank()) {
            JOptionPane.showMessageDialog(this, "Plese Enter the Hight", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField2.grabFocus();
        } else if (weight.isBlank()) {
            JOptionPane.showMessageDialog(this, "Plese Enter the Weight", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField3.grabFocus();
        } else if (schedule.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Plese Select the Schedule", "Warning", JOptionPane.WARNING_MESSAGE);
            jComboBox1.grabFocus();
        } else if (payment.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Plese Select the Payment", "Warning", JOptionPane.WARNING_MESSAGE);
            jComboBox2.grabFocus();
        } else {
            try {

                ResultSet memberrs = MySQL.search("SELECT * FROM `players` WHERE `id` = '" + member_id + "' AND status_id != '2'");
                if (memberrs.next()) {

                    SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                    String date = sdf.format(new Date());

                    ResultSet paymentTypers = MySQL.search("SELECT `id` FROM `player_payment_type` WHERE `price` = '" + payment + "'");
                    String payment_id = "";
                    if (paymentTypers.next()) {
                        payment_id = paymentTypers.getString("id");
                    }

                    String mounthly_payment_id = String.valueOf(System.currentTimeMillis());

                    MySQL.iud("INSERT INTO `monthly_payment` (`id`,`date`,`player_payment_type_id`) VALUES ('" + mounthly_payment_id + "','" + date + "','" + payment_id + "')");

                    if (medical.isBlank()) {
                        medical = "None";
                    }

                    ResultSet schedulers = MySQL.search("SELECT DISTINCT(`schedule`.`name`),`schedule`.`id` FROM `schedule` WHERE `name` = '" + schedule + "'");
                    String schedule_id = "";
                    if (schedulers.next()) {
                        schedule_id = schedulers.getString("id");
                    }

                    MySQL.iud("INSERT INTO `players_prograss` (`players_id`,`hight`,`weight`,`date`,`medical`,`schedule_id`,`monthly_payment_id`) VALUES('" + member_id + "','" + hight + "','" + weight + "','" + date + "','" + medical + "','" + schedule_id + "','" + mounthly_payment_id + "')");
                    resetFields();
                    JOptionPane.showMessageDialog(this, "Mounthly payment Added", "Success", JOptionPane.INFORMATION_MESSAGE);

                } else {

                    resetFields();
                    JOptionPane.showMessageDialog(this, "Incorrct Member ID or This Member is Inavtive", "Warning", JOptionPane.WARNING_MESSAGE);

                }

            } catch (Exception e) {
                e.printStackTrace();
            }
        }

    }//GEN-LAST:event_jButton2ActionPerformed

