    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        
        int selectedrow = jTable1.getSelectedRow();

        if (evt.getClickCount() == 1) {

            jTextField1.setText(jTable1.getValueAt(selectedrow, 1).toString());

        } else if (evt.getClickCount() == 2) {

            int option = JOptionPane.showConfirmDialog(this, "Do You  Want to Delete this Category", "Warning", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);

            if (option == JOptionPane.YES_OPTION) {
                MySQL.iud("DELETE FROM `machine_category` WHERE `id` = '" + jTable1.getValueAt(selectedrow, 0).toString() + "'");
                jTextField1.setText("");
                loadTable();
//                JOptionPane.showMessageDialog(this, "Category was Deleted", "Success", JOptionPane.INFORMATION_MESSAGE);
            }
        }
        
    }//GEN-LAST:event_jTable1MouseClicked

