    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String name = jTextField1.getText();
        String mobile = jTextField2.getText();
        String address = jTextField3.getText();
        String city = jComboBox1.getSelectedItem().toString();
        
        try {
            ResultSet rs = MySQL.search("SELECT * FROM `city` WHERE `name` = '"+city+"'");
            String city_id = "";
            if (rs.next()) {
                city_id = rs.getString("id");
            }
            MySQL.iud("INSERT INTO `customer` (`name`,`mobile`,`address`,`city_id`) VALUES('"+name+"','"+mobile+"','"+address+"','"+city_id+"')");
            resetFields();
            loadTable();
            JOptionPane.showMessageDialog(this, "Customer Added", "Success", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

