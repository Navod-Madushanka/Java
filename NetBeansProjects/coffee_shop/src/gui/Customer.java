    public void loadTable(){
        try {
            ResultSet rs = MySQL.search("SELECT `customer`.`id`, `customer`.`name`, `customer`.`mobile`, `customer`.`address`, `city`.`name` AS `city_name` FROM `customer` INNER JOIN `city` ON `customer`.`city_id` = `city`.`id` ORDER BY `customer`.`id` ASC");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            while(rs.next()){
                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("name"));
                v.add(rs.getString("mobile"));
                v.add(rs.getString("address"));
                v.add(rs.getString("city_name"));
                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
