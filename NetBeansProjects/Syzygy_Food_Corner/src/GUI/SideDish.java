    private void fillComboBox(){
        jComboBox1.removeAllItems();
        for(SideDishes dishes : SideDishes.values()){
            jComboBox1.addItem(dishes.name());
        }
    }
    private void fillTablel(){
        DefaultTableModel model = new DefaultTableModel(new Object[]{"Dish Name", "Price"}, 0);
        for (MainDishes dish : MainDishes.values()) {
            Object[] rowData = {dish.name(), dish.getPrice()};
            model.addRow(rowData);
        }
        jTable1.setModel(model);
    }

