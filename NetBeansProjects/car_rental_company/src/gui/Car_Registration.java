    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:

        String type = jComboBox1.getSelectedItem().toString();
        String brand_id = jLabel4.getText();
        String model_id = jLabel8.getText();
        String owner_nic = jLabel14.getText();
        String num_plate = jTextField1.getText();

        if (type.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select the car type", "Warning", JOptionPane.WARNING_MESSAGE);
            jComboBox1.grabFocus();
        } else if (brand_id.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please Select the car brand", "Warning", JOptionPane.WARNING_MESSAGE);
            jButton1.grabFocus();
        } else if (model_id.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please Select the car Model", "Warning", JOptionPane.WARNING_MESSAGE);
            jButton2.grabFocus();
        } else if (num_plate.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please Enter the car Number Plate", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField1.grabFocus();
        } else if (owner_nic.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please Select the Owner", "Warning", JOptionPane.WARNING_MESSAGE);
            jButton3.grabFocus();
        } else {
            try {
                ResultSet rs = MySQL.search("SELECT * FROM `car` WHERE `num_plate` = '" + num_plate + "'");
                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "This car is already Added to the System", "Warning", JOptionPane.WARNING_MESSAGE);
                    resetFields();
                } else {

                    ResultSet typers = MySQL.search("SELECT `id` FROM `category` WHERE `name` = '" + type + "'");
                    String type_id = "";
                    if (typers.next()) {
                        type_id = typers.getString("id");
                    }

                    SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                    String date = sdf.format(new Date());

                    MySQL.iud("INSERT INTO `car` (`category_id`,`brand_id`,`model_id`,`num_plate`,`date`,`owner_nic`) VALUES('" + type_id + "','" + brand_id + "','" + model_id + "','" + num_plate + "','" + date + "','" + owner_nic + "')");
                    resetFields();
                    loadtable();
                    JOptionPane.showMessageDialog(this, "New Car Added", "Success", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }

    }//GEN-LAST:event_jButton4ActionPerformed

