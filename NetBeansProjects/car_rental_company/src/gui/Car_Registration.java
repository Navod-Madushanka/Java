    public void loadtable() {
        try {
            ResultSet rs = MySQL.search("SELECT `car`.`num_plate`, `category`.`name`, `brand`.`name` AS `brand_name`, `model`.`name` AS `model_name`, `car`.`owner_nic`, `car`.`date` FROM `car` INNER JOIN `category` ON `car`.`category_id` = `category`.`id` INNER JOIN `brand` ON `car`.`brand_id` = `brand`.`id` INNER JOIN `model` ON `car`.`model_id` = `model`.`id` ORDER BY `car`.`num_plate` ASC");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("num_plate"));
                v.add(rs.getString("name"));
                v.add(rs.getString("brand_name"));
                v.add(rs.getString("model_name"));
                v.add(rs.getString("owner_nic"));
                v.add(rs.getString("date"));
                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

