    public void loadsuplier() {
        try {
            ResultSet rs = MySQL.search("SELECT `supplier`.`id`, `supplier`.`name`, `supplier`.`contact_number`, `supplier`.`email`, `company_branch`.`name` AS `branch_name`, `company_branch`.`branch_contact_number` AS `branch_contact_number`, `company_branch_address`.`line_1`, `company_branch_address`.`line_2`, `city`.`name` AS `city_name` FROM `supplier` INNER JOIN `company_branch` ON `supplier`.`company_branch_id` = `company_branch`.`id` INNER JOIN `company_branch_address` ON `company_branch`.`company_branch_address_id` = `company_branch_address`.`id` INNER JOIN `city` ON `company_branch_address`.`city_id` = `city`.`id` ORDER BY `supplier`.`id` ASC");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("name"));
                v.add(rs.getString("contact_number"));
                v.add(rs.getString("email"));
                v.add(rs.getString("branch_name"));
                v.add(rs.getString("branch_contact_number"));
                String address = rs.getString("line_1") + "," + rs.getString("line_2") + "," + rs.getString("city_name");
                v.add(address);
                dtm.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

