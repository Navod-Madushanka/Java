    public void loadCity() {
        try {
            ResultSet rs = MySQL.search("SELECT* FROM `city`");

            Vector v = new Vector();
            v.add("Select");

            while (rs.next()) {
                v.add(rs.getString("name"));
            }

            DefaultComboBoxModel dcm = new DefaultComboBoxModel(v);
            jComboBox1.setModel(dcm);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void loadCompanyBranches() {

        try {
            ResultSet rs = MySQL.search("SELECT `company`.`id`, `company`.`name`,`company_branch`.`id` AS `branch_id`, `company_branch`.`name` AS `branch_name`, `company_branch`.`branch_contact_number`, `company_branch_address`.`line_1`, `company_branch_address`.`line_2`, `city`.`name` AS `city_name` FROM `company_branch` INNER JOIN `company` ON `company_branch`.`company_id` = `company`.`id` INNER JOIN `company_branch_address` ON `company_branch`.`company_branch_address_id` = `company_branch_address`.`id` INNER JOIN `city` ON `company_branch_address`.`city_id` = `city`.`id` ORDER BY `company`.`id` ASC, `company_branch`.`id` ASC ");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("name"));
                v.add(rs.getString("branch_id"));
                v.add(rs.getString("branch_name"));
                v.add(rs.getString("branch_contact_number"));
                String address = rs.getString("line_1") + "," + rs.getString("line_2") + "," + rs.getString("city_name");
                v.add(address);
                dtm.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

