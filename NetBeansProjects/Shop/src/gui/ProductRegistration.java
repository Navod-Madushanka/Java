    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String pname = jTextField1.getText();
        String brand = jComboBox1.getSelectedItem().toString();
        String category = jComboBox2.getSelectedItem().toString();

        if (pname.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please Enter the Product Name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (brand.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select the Brand Name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (category.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Enter the Category Name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ResultSet rs = MySQL.search("SELECT * FROM `product` WHERE `name` = '" + pname + "'");
                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "This Product is Already Exsits", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    String brand_id = null;
                    String category_id = null;

                    ResultSet rsb = MySQL.search("SELECT `id` FROM `brand` WHERE `name` = '" + brand + "'");
                    if (rsb.next()) {
                        brand_id = rsb.getString("id");
                    }
                    ResultSet rsc = MySQL.search("SELECT `id` FROM `category` WHERE `name` = '" + category + "'");
                    if (rsc.next()) {
                        category_id = rsc.getString("id");
                    }
                    MySQL.iud("INSERT INTO `product`(`category_id`,`brand_id`,`name`) VALUES('" + category_id + "','" + brand_id + "','" + pname + "')");
                    JOptionPane.showMessageDialog(this, "New Product Added", "Success", JOptionPane.INFORMATION_MESSAGE);
                    loadProduct();
                }
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            int r = jTable1.getSelectedRow();
            if (r == -1) {
                JOptionPane.showMessageDialog(this, "Please Select A Product", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {
                String pid = jTable1.getValueAt(r, 0).toString();
                String pname = jTable1.getValueAt(r, 1).toString();
                String brand = jTable1.getValueAt(r, 2).toString();
                String category = jTable1.getValueAt(r, 3).toString();

                grn.jLabel10.setText(pid);
                grn.jLabel12.setText(pname);
                grn.jLabel14.setText(brand);
                grn.jLabel16.setText(category);

                grn.jTextField2.grabFocus();

                this.dispose();
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

