    public void loadProduct() {
        try {
            ResultSet rs = MySQL.search("SELECT `product`.`id`, `product`.`name` as `pname`, `brand`.`name` as `bname`, `category`.`name` as `cname` FROM `product` INNER JOIN `category` ON `product`.`category_id` = `category`.`id` INNER JOIN `brand` ON `product`.`brand_id` = `brand`.`id`");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();

                v.add(rs.getString("id"));
                v.add(rs.getString("pname"));
                v.add(rs.getString("bname"));
                v.add(rs.getString("cname"));

                dtm.addRow(v);
            }

        } catch (Exception e) {
        }
    }

