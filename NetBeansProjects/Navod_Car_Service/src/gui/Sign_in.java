    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String user_type = jComboBox1.getSelectedItem().toString();
        String username = jTextField1.getText();
        char[] passwordarray = jPasswordField1.getPassword();
        String password = String.valueOf(passwordarray);

        if (user_type.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select A User Type", "Warning", JOptionPane.WARNING_MESSAGE);
            jComboBox1.grabFocus();
        } else if (username.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please Enter the User Name", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField1.grabFocus();
        } else if (password.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please Enter the User Name", "Warning", JOptionPane.WARNING_MESSAGE);
            jPasswordField1.grabFocus();
        } else {

            try {
                if (user_type.equals("Admin")) {
                    ResultSet rs = MySQL.search("SELECT * FROM `admin` WHERE `username` = '" + username + "' AND `password` = '" + password + "'");
                    if (rs.next()) {
                         new Admin_Home().setVisible(true);
                    } else {
                        JOptionPane.showMessageDialog(this, "Invalied User Name or Password", "Warning", JOptionPane.WARNING_MESSAGE);
                        reloadFields();
                        jTextField1.grabFocus();
                    }
                } else {

                }
            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_jButton1ActionPerformed

