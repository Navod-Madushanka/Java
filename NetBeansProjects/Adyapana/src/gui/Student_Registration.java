    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        String name = jTextField1.getText();

        Date date = jDateChooser1.getDate();

        String address = jTextField2.getText();
        String city = jComboBox1.getSelectedItem().toString();
        String subject = jComboBox2.getSelectedItem().toString();

        if (name.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please Enter Student Name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (date == null) {
            JOptionPane.showMessageDialog(this, "Please Enter Student's Birthday", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (address.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please Enter Address", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (city.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select A City", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (subject.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select A Subject", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {
                String unq = String.valueOf(System.currentTimeMillis());
                String bod = sdf.format(date);
//                System.out.println(bod);

                ResultSet rs1 = MySQL.search("SELECT `Subno` FROM `subject` WHERE `Description` = '" + subject + "'");
                if (rs1.next()) {
                    MySQL.iud("INSERT INTO `student`(`Sno`,`name`,`subject_Subno`,`bod`) VALUES('" + unq + "','" + name + "','" + rs1.getString("Subno") + "','" + bod + "')");

                    ResultSet rs = MySQL.search("SELECT * FROM `city` WHERE `name` = '" + city + "'");
                    if (rs.next()) {
                        MySQL.iud("INSERT INTO `address`(`student_Sno`,`line`,`city_id`) VALUES('" + unq + "','" + address + "','" + rs.getString("id") + "')");
                    }
                }

                loadTable();
                clearFields();
                JOptionPane.showMessageDialog(this, "New Student Added", "Success", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_jButton3ActionPerformed

