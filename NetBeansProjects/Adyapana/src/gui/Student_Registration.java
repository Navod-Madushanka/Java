    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            selectedrow = jTable1.getSelectedRow();

            jLabel3.setText("Update Student");
            jButton3.setEnabled(false);
            jButton2.setEnabled(true);
            jButton4.setEnabled(true);
            jDateChooser1.setEnabled(false);

            try {
                String name = jTable1.getValueAt(selectedrow, 1).toString();
                sid = jTable1.getValueAt(selectedrow, 0).toString();
                status = jTable1.getValueAt(selectedrow, 4).toString();
                jTextField1.setText(name);
                ResultSet rs = MySQL.search("SELECT `address`.`line`, `city`.`name` FROM `address` INNER JOIN `city` ON `address`.`city_id` = `city`.`id` WHERE `student_Sno` = '" + jTable1.getValueAt(selectedrow, 0).toString() + "'");
                if (rs.next()) {
                    String line = rs.getString("line");
                    String city = rs.getString("name");
                    jTextField2.setText(line);
                    jComboBox1.setSelectedItem(city);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }

        }else if(evt.getClickCount() == 3){
            in.jLabel2.setText(jTable1.getValueAt(selectedrow, 0).toString());
            in.jLabel4.setText(jTable1.getValueAt(selectedrow, 1).toString());
            in.jLabel6.setText(jTable1.getValueAt(selectedrow, 2).toString());
            in.loadTable1();
            this.dispose();
        }
    }//GEN-LAST:event_jTable1MouseClicked

