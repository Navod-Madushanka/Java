    public void loadTable() {
        try {
            ResultSet rs = MySQL.search("SELECT `student`.`Sno`, `student`.`name`, `student`.`bod`, `address`.`line`, `city`.`name` AS `city_name`, `status`.`status`, `subject`.`Description` FROM `student` INNER JOIN `address` ON `student`.`Sno` = `address`.`student_Sno` INNER JOIN `city` ON `address`.`city_id` = `city`.`id` INNER JOIN `status` ON `student`.`status_id` = `status`.`id` INNER JOIN `subject` ON `student`.`subject_Subno` = `subject`.`Subno` ORDER BY `student`.`Sno` ASC");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("Sno"));
                v.add(rs.getString("name"));
                v.add(rs.getString("bod"));
                v.add(rs.getString("Description"));
                v.add(rs.getString("line") + ", " + rs.getString("city_name"));
                v.add(rs.getString("status"));
                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

