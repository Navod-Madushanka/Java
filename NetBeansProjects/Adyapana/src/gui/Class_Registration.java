    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String search_field = jTextField1.getText();

        try {
            ResultSet rs = MySQL.search("SELECT `class`.`classno`, `subject`.`Description`, `teacher`.`name`, `class`.`date`, `class`.`time`, `am_pm`.`type` FROM `class` INNER JOIN `subject` ON `class`.`subject_Subno` = `subject`.`Subno` INNER JOIN `teacher` ON `class`.`teacher_tno` = `teacher`.`tno` INNER JOIN `am_pm` ON `class`.`am_pm_id` = `am_pm`.`id` WHERE `subject`.`Description` = '" + search_field + "' ORDER BY `class`.`classno` DESC");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("classno"));
                v.add(rs.getString("Description"));
                v.add(rs.getString("name"));
                v.add(rs.getString("date"));
                v.add(rs.getString("time") + "." + rs.getString("type"));
                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton4ActionPerformed

