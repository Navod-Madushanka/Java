    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String subject = jComboBox1.getSelectedItem().toString();
        Date date = jDateChooser1.getDate();
        String time = jTextField2.getText();
        String teacher_id = jLabel10.getText();

        int am_pm = 0;
        if (jRadioButton1.isSelected()) {
            am_pm = 1;
        } else if (jRadioButton2.isSelected()) {
            am_pm = 2;
        }

        if (subject.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select the Subject", "Warning", JOptionPane.WARNING_MESSAGE);
            jComboBox1.grabFocus();
        } else if (date == null) {
            JOptionPane.showMessageDialog(this, "Please Select a Date", "Warning", JOptionPane.WARNING_MESSAGE);
            jDateChooser1.grabFocus();
        } else if (time.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please Enter the Time", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField2.grabFocus();
        } else if (!Pattern.compile("[0-9][0-9]:[0-9][0-9]").matcher(time).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid Time", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField2.grabFocus();
        } else if (am_pm == 0) {
            JOptionPane.showMessageDialog(this, "Please Select AM or PM", "Warning", JOptionPane.WARNING_MESSAGE);
            jRadioButton1.grabFocus();
        } else if (teacher_id.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please Select A Teacher", "Warning", JOptionPane.WARNING_MESSAGE);
            jButton6.grabFocus();
        } else {

            try {

                String class_id = String.valueOf(System.currentTimeMillis());

                ResultSet rs = MySQL.search("SELECT * FROM `teacher` INNER JOIN subject ON teacher.subject_Subno = subject.Subno WHERE `tno` = '" + teacher_id + "'");
                if (rs.next()) {
                    if (subject.equals(rs.getString("Description"))) {

                        MySQL.iud("INSERT INTO `class` (`classno`,`subject_Subno`,`teacher_tno`,`date`,`time`,`am_pm_id`) VALUES('" + class_id + "','" + rs.getString("Subno") + "','" + teacher_id + "','" + sdf.format(date) + "','" + time + "','" + am_pm + "')");

                        ResultSet rs2 = MySQL.search("SELECT * FROM `student` WHERE `subject_Subno` = '" + rs.getString("Subno") + "'");

                        while (rs2.next()) {
                            MySQL.iud("INSERT INTO `student_has_class` (`student_Sno`,`class_classno`) VALUES('" + rs2.getString("Sno") + "','" + class_id + "')");
                        }

                        clearinsertFields();
                        JOptionPane.showMessageDialog(this, "New Class is Added", "Success", JOptionPane.INFORMATION_MESSAGE);

                    } else {
                        JOptionPane.showMessageDialog(this, "Invalid Teacher", "Error", JOptionPane.ERROR_MESSAGE);
                        jButton6.grabFocus();
                    }
                }

            } catch (Exception e) {
                e.printStackTrace();
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

