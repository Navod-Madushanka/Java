    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 1) {
            selectedrow = jTable1.getSelectedRow();
            classid = jTable1.getValueAt(selectedrow, 0).toString();
            clearupdateFields();

            try {
                jComboBox3.setSelectedItem(jTable1.getValueAt(selectedrow, 1).toString());

                ResultSet rs = MySQL.search("SELECT `class`.`classno`, `subject`.`Description`, `teacher`.`tno`,`teacher`.`name`, `class`.`date`, `class`.`time`, `am_pm`.`type` FROM `class` INNER JOIN `subject` ON `class`.`subject_Subno` = `subject`.`Subno` INNER JOIN `teacher` ON `class`.`teacher_tno` = `teacher`.`tno` INNER JOIN `am_pm` ON `class`.`am_pm_id` = `am_pm`.`id` WHERE `class`.`classno` = '" + jTable1.getValueAt(selectedrow, 0) + "'");
                if (rs.next()) {
                    jTextField4.setText(rs.getString("time"));

                    if (rs.getString("type").equals("am")) {
                        jRadioButton3.setSelected(true);
                    } else {
                        jRadioButton4.setSelected(true);
                    }

                    jLabel13.setText(rs.getString("tno"));
                    jLabel15.setText(rs.getString("name"));

                }

            } catch (Exception e) {
                e.printStackTrace();
            }
//            jTable1.clearSelection();
        }
    }//GEN-LAST:event_jTable1MouseClicked

