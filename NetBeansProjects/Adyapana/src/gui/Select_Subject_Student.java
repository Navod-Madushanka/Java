    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String search_subject = jTextField1.getText();
        if (!search_subject.isBlank()) {
            try {
                ResultSet rs = MySQL.search("SELECT * FROM `subject` WHERE `Description` = '" + search_subject + "'");
                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                dtm.setRowCount(0);
                while (rs.next()) {
                    Vector v = new Vector();
                    v.add(rs.getString("Subno"));
                    v.add(rs.getString("Description"));
                    v.add(rs.getString("price"));
                    dtm.addRow(v);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please Enter Subject You Search", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField1.grabFocus();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

