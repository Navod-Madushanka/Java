    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:

        String subject_id = "";
        if (evt.getClickCount() == 1) {
            int selectedrow = jTable1.getSelectedRow();
            Date d = new Date();

            if (subject_id.equals(jTable1.getValueAt(selectedrow, 0))) {
                JOptionPane.showMessageDialog(this, "This Subject is already Added", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {
                MySQL.iud("INSERT INTO `student_has_subject`(`student_Sno`,`subject_Subno`) VALUES('" + in.jLabel2.getText() + "','" + jTable1.getValueAt(selectedrow, 0) + "')");
                MySQL.iud("INSERT INTO `invoice` (`student_Sno`,`subject_Subno`,`month`,`value`,`balance`,`payment_id`) VALUES('" + in.jLabel2.getText() + "','" + jTable1.getValueAt(selectedrow, 0) + "','" + sdf.format(d) + "','" + jTable1.getValueAt(selectedrow, 2) + "','" + jTable1.getValueAt(selectedrow, 2) + "','1')");
                subject_id = jTable1.getValueAt(selectedrow, 0).toString();
                JOptionPane.showMessageDialog(this, "Subject Is Added", "Success", JOptionPane.INFORMATION_MESSAGE);
                in.loadTable1();
            }

        }
    }//GEN-LAST:event_jTable1MouseClicked

