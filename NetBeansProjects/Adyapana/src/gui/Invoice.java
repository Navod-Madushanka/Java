    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int rowcount = jTable1.getRowCount();

        DefaultTableModel dtm = (DefaultTableModel) jTable2.getModel();
        dtm.setRowCount(0);

        for (int i = 0; i < rowcount; i++) {
            Vector v = new Vector();
            v.add(jTable1.getValueAt(i, 0));
            v.add(jTable1.getValueAt(i, 1));
            v.add(jTable1.getValueAt(i, 2));
            v.add(jTable1.getValueAt(i, 2));
            dtm.addRow(v);
        }
        jButton5.setEnabled(true);

        double price = 0;

        try {
            ResultSet rs = MySQL.search("SELECT * FROM `invoice` WHERE `invoice`.`student_Sno` = '" + jLabel2.getText() + "'");
            while (rs.next()) {
                price = price + Double.parseDouble(String.valueOf(rs.getString("balance")));
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton4ActionPerformed

