    public void payment() {
        try {
            ResultSet rs = MySQL.search("SELECT * FROM `payment`");
            Vector v = new Vector();

            while (rs.next()) {
                v.add(rs.getString("name"));
            }
            DefaultComboBoxModel dcm = new DefaultComboBoxModel(v);
            jComboBox1.setModel(dcm);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void updateTotal() {
        double total = 0;

        for (int i = 0; i < jTable1.getRowCount(); i++) {
            String t = jTable1.getValueAt(i, 3).toString();
            total = total + Double.parseDouble(t);
        }

        jLabel9.setText(df.format(total));
    }

    public void loadTable1() {
        try {
            ResultSet rs = MySQL.search("SELECT `subject`.`Subno`, `subject`.`Description`, `invoice`.`value`, `invoice`.`balance` FROM `student_has_subject` INNER JOIN `student` ON `student_has_subject`.`student_Sno` = `student`.`Sno` INNER JOIN `subject` ON `student_has_subject`.`subject_Subno` = `subject`.`Subno` INNER JOIN `invoice` ON `student_has_subject`.`student_Sno` = `invoice`.`student_Sno` WHERE `student_has_subject`.`student_Sno` = '" + jLabel2.getText() + "'");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("Subno"));
                v.add(rs.getString("Description"));
                v.add(rs.getString("value"));
                v.add(rs.getString("balance"));
                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        this.updateTotal();
        jButton2.setEnabled(true);
    }

