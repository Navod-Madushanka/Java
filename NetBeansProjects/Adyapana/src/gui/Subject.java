    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String new_subject = jTextField1.getText();
        String price = jTextField3.getText();
        if (new_subject.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please Enter Subject", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField1.grabFocus();
        } else if (price.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please Enter Price", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField3.grabFocus();
        } else {
            try {
                ResultSet rs = MySQL.search("SELECT * FROM `subject` WHERE `Description` = '" + new_subject + "'");
                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "This Subject is Already Excits", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    MySQL.iud("INSERT INTO `subject`(`Description`,`price`) VALUES('" + new_subject + "','" + price + "')");
                    JOptionPane.showMessageDialog(this, "New Subject Added", "Success", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
            jTextField1.setText("");
            jTextField3.setText("");
            jTextField1.grabFocus();
        }
        loadTable();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTextField3KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField3KeyTyped
        // TODO add your handling code here:

        String price = jTextField3.getText();
        String text = price + evt.getKeyChar();

        if (!Pattern.compile("(0|0[.]|0[.][0-9]*)|[1-9]|[1-9][0-9]*|[1-9][0-9]*[.]|[1-9][0-9]*[.][0-9]*").matcher(text).matches()) {
            evt.consume();
        }
    }//GEN-LAST:event_jTextField3KeyTyped

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String search_subject = jTextField2.getText();
        if (!search_subject.isBlank()) {
            try {
                ResultSet rs = MySQL.search("SELECT * FROM `subject` WHERE `Description` = '" + search_subject + "'");
                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                dtm.setRowCount(0);
                while (rs.next()) {
                    Vector v = new Vector();
                    v.add(rs.getString("Subno"));
                    v.add(rs.getString("Description"));
                    v.add(rs.getString("price"));
                    dtm.addRow(v);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please Enter Subject You Search", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField2.grabFocus();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

