    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int selected_row;
        if (evt.getClickCount() == 2) {
            selected_row = jTable1.getSelectedRow();
            if (selected_row == -1) {
                JOptionPane.showMessageDialog(this, "Pleace Select A Subject", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {
                String subject_id = jTable1.getValueAt(selected_row, 0).toString();
                String subject = jTable1.getValueAt(selected_row, 1).toString();

                tr.jLabel6.setText(subject_id);
                tr.jLabel8.setText(subject);
                tr.jButton1.setEnabled(false);
                tr.jButton1.setText("Change Subject");
                this.dispose();
            }
        } else if (evt.getButton() == evt.BUTTON3) {
            selected_row = jTable1.getSelectedRow();
            int option = JOptionPane.showConfirmDialog(this, "Do You want to Remove This Subject", "Warning", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);

            if (option == JOptionPane.YES_OPTION) {
                MySQL.iud("DELETE FROM `subject` WHERE `Subno` = '" + jTable1.getValueAt(selected_row, 0).toString() + "'");
                JOptionPane.showMessageDialog(this, "New Subject Added", "Success", JOptionPane.INFORMATION_MESSAGE);
            }
            loadTable();
        }else{
            selected_row = jTable1.getSelectedRow();
            jButton1.setEnabled(false);
            jTextField1.setEnabled(false);
            
            jTextField1.setText(jTable1.getValueAt(selected_row, 1).toString());
            jTextField3.setText(jTable1.getValueAt(selected_row, 2).toString());
        }

    }//GEN-LAST:event_jTable1MouseClicked

